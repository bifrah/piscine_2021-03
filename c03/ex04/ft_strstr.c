char	*ft_strstr(char *str, char *to_find)
{
	int i;
	int j;

	i = 0;
	j = 0;
	if (to_find[0] == 0)
		return (str);
	while (str[i])
	{
		j = 0;
		while (to_find[j] == str[i + j])
		{
			if (to_find[j + 1] == '\0')
				return (str + i);
			j++;
		}
		i++;
	}
	return (0);
}

#include <stdio.h>
#include <string.h>

int main()
{
	char str[] = "salut";
	char tf[] = "";

	printf("%s", ft_strstr(str, tf));
}
