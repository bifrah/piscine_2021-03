int		ft_is_prime(int nb)
{
	int div;
	int result;

	div = 2;
	result = 0;
	if (nb <= 1)
		return (0);
	while (div <= nb / 2)
	{
		result = nb % div;
		if (result == 0)
			return (0);
		div++;
	}
	return (1);
}
